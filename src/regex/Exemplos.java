    /*
    * Modificadores
    *   (?i) -- Procura a ocorrência ignorando maiúsculas e minúsculas
    *   (?x) -- Comentários
    *   (?m) -- Multilinhas
    *   (?s) -- Dottal
    *   */


    /*
    * Metacaracteres
    *   .   -- Procura a ocorrência de qualquer tipo de caracter
    *   \\d  -- dígitos  [0-9]
    *   \\D  -- não é dígito  [^0-9]
    *   \\s  -- espaços,tabulação,recuo   [\t \n \x0B \f \r]
    *   \\S  -- não é espaço   [^s]
    *   \\w  --letra   [a-zA-Z_0-9]
    *   \\W  --não é letra   [^w]
    *   */


    /*
    *Quantificadores
    *   X{n} -- Procura a ocorrência de uma expressão regular "X" EXACTAMENTE N vezes
    *   X{n,} -- Pelo menos N vezes
    *   X{n,m} -- No intervalo de M a N vezes
    *   X?  -- 0 ou 1 vez
    *   X*  -- 0 ou mais vezes
    *   X+  -- 1 ou mais vezes
    *   */


